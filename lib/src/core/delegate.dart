import 'package:dimp/crypto.dart';
import 'package:dimp/dkd.dart';
import 'package:dimp/mkm.dart';


/// Delegate interface for managing **directional symmetric encryption keys** in messaging.
///
/// Core concept: Symmetric keys are direction-specific (unidirectional) between two parties:
/// - User A → User B: Uses key1 (generated by A, for A→B messages only)
/// - User B → User A: Uses key2 (generated by B, for B→A messages only)
/// - Key1 ≠ Key2, both exist simultaneously during A↔B communication
/// - Independent key pairs are generated for A↔C, B↔C, etc.
///
/// Key features:
/// 1. Directionality: Keys are scoped to (sender → receiver) pairs (not bidirectional)
/// 2. Reusability: Cached keys can be reused for subsequent messages in the same direction
/// 3. Context awareness: Handles personal/group/broadcast message key management
abstract interface class CipherKeyDelegate {

    /*  Message scenario matrix (key destination determination):

                      +-------------+-------------+-------------+-------------+
                      |  receiver   |  receiver   |  receiver   |  receiver   |
                      |     is      |     is      |     is      |     is      |
                      |             |             |  broadcast  |  broadcast  |
                      |    user     |    group    |    user     |    group    |
        +-------------+-------------+-------------+-------------+-------------+
        |             |      A      |             |             |             |
        |             +-------------+-------------+-------------+-------------+
        |    group    |             |      B      |             |             |
        |     is      |-------------+-------------+-------------+-------------+
        |    null     |             |             |      C      |             |
        |             +-------------+-------------+-------------+-------------+
        |             |             |             |             |      D      |
        +-------------+-------------+-------------+-------------+-------------+
        |             |      E      |             |             |             |
        |             +-------------+-------------+-------------+-------------+
        |    group    |             |             |             |             |
        |     is      |-------------+-------------+-------------+-------------+
        |  broadcast  |             |             |      F      |             |
        |             +-------------+-------------+-------------+-------------+
        |             |             |             |             |      G      |
        +-------------+-------------+-------------+-------------+-------------+
        |             |      H      |             |             |             |
        |             +-------------+-------------+-------------+-------------+
        |    group    |             |      J      |             |             |
        |     is      |-------------+-------------+-------------+-------------+
        |    normal   |             |             |      K      |             |
        |             +-------------+-------------+-------------+-------------+
        |             |             |             |             |             |
        +-------------+-------------+-------------+-------------+-------------+

        Scenario explanations:
          A: Personal message (1:1, directional key)
          B: (same to J)
          C: Broadcast to individual user (no encryption)
          D: (same to G)
          E: Broadcast group message to individual user (no encryption)
          F: Broadcast group message to any user (no encryption)
          G: Broadcast group message (receiver=group, no encryption, non-split)
          H: Group message targeted to individual member (split key)
          J: Standard group message (receiver=group, non-split)
          K: Unencrypted group command (broadcast receiver + normal group)
     */

  /// Derives the key destination ID for a message (determines key scope).
  ///
  /// Maps the message's receiver/group context to a standard destination ID
  /// for key lookup/caching (follows the scenario matrix above).
  ///
  /// Parameters:
  /// - [msg] : Message to get key destination for
  ///
  /// Returns: Standardized destination ID for key management
  static ID getDestinationForMessage(Message msg) =>
      getDestination(receiver: msg.receiver, group: ID.parse(msg['group']));

  /// Core method to determine key destination ID from receiver/group context.
  ///
  /// Handles scenario transformations:
  /// 1. If group is null but receiver is group → map to group ID (B→J, D→G)
  /// 2. If group is null → use receiver (personal/broadcast user message: A/C)
  /// 3. If group is broadcast → use group ID (broadcast scenarios: E/F/G)
  /// 4. If receiver is broadcast → use receiver (unencrypted group command: K)
  /// 5. Otherwise → use group ID (standard group scenarios: H/J)
  ///
  /// Parameters:
  /// - [receiver] : Target receiver ID (user/group/broadcast)
  /// - [group]    : Optional group ID (null for personal messages)
  ///
  /// Returns: Standardized destination ID for key management
  ///
  /// Throws: Assertion error if ID types are invalid (e.g., non-user receiver for personal message)
  static ID getDestination({required ID receiver, required ID? group}) {
    if (group == null && receiver.isGroup) {
      /// Transform:
      ///     (B) => (J)
      ///     (D) => (G)
      group = receiver;
    }
    if (group == null) {
      /// A : personal message (or hidden group message)
      /// C : broadcast message for anyone
      assert(receiver.isUser, 'receiver error: $receiver');
      return receiver;
    }
    assert(group.isGroup, 'group error: $group, receiver: $receiver');
    if (group.isBroadcast) {
      /// E : unencrypted message for someone
      //      return group as broadcast ID for disable encryption
      /// F : broadcast message for anyone
      /// G : (receiver == group) broadcast group message
      assert(receiver.isUser || receiver == group, 'receiver error: $receiver');
      return group;
    } else if (receiver.isBroadcast) {
      /// K : unencrypted group message, usually group command
      //      return receiver as broadcast ID for disable encryption
      assert(receiver.isUser, 'receiver error: $receiver, group: $group');
      return receiver;
    } else {
      /// H    : group message split for someone
      /// J    : (receiver == group) non-split group message
      return group;
    }
  }

  /// Retrieves the directional symmetric key for (sender → receiver) communication.
  ///
  /// Key rules:
  /// - Returns the key specific to the sender→receiver direction (not reverse)
  /// - If key doesn't exist and [generate] is true → creates a new random key
  /// - If key doesn't exist and [generate] is false → returns null
  ///
  /// Parameters:
  /// - [sender]   : Source user/contact ID (defines key direction origin)
  /// - [receiver] : Target user/group ID (defines key direction target)
  /// - [generate] : Whether to generate a new key if none exists (default: false)
  ///
  /// Returns: Directional symmetric key for sender→receiver (null if not found and not generated)
  Future<SymmetricKey?> getCipherKey({required ID sender, required ID receiver,
                                      bool generate = false});

  /// Caches a directional symmetric key for reuse in (sender → receiver) communication.
  ///
  /// Key rules:
  /// - Stores the key under the exact (sender, receiver) pair (directional)
  /// - Overwrites any existing key for the same direction (sender→receiver)
  /// - Does not affect the reverse direction (receiver→sender) key
  ///
  /// Parameters:
  /// - [sender]   : Source user/contact ID (key direction origin)
  /// - [receiver] : Target user/group ID (key direction target)
  /// - [key]      : Symmetric key to cache (must be unique for this direction)
  ///
  /// Returns: Future that completes when caching is done (no return value)
  Future<void> cacheCipherKey({required ID sender, required ID receiver,
                               required SymmetricKey key});

}
